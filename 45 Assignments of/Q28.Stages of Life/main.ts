const personsAge = 18;
if (personsAge >= 2 && personsAge < 4) {
    console.log(' person is a toddler');
}
if (personsAge >= 3 && personsAge < 13) {
    console.log(' person is a Kid');
}
if (personsAge >= 13 && personsAge < 20) {
    console.log(' person is a Teenager');
}
if (personsAge >= 20 && personsAge < 65) {
    console.log(' person is an Adult');
}
if (personsAge >= 65) {
    console.log(' person is an Elder');
}